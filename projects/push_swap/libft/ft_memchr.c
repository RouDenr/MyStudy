#include <stdlib.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*buf;
	unsigned char		unc;
	size_t				i;

	i = 0;
	buf = s;
	unc = c;
	while (n-- > 0)
		if (buf[i++] == unc)
			return ((void *)&buf[--i]);
	return (NULL);
}
